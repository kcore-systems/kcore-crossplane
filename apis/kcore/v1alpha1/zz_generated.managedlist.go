package v1alpha1

import resource "github.com/crossplane/crossplane-runtime/v2/pkg/resource"

func (l *VirtualMachineList) GetItems() []resource.Managed {
	items := make([]resource.Managed, len(l.Items))
	for i := range l.Items {
		items[i] = &l.Items[i]
	}
	return items
}

